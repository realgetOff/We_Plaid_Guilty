func socketLogic(conn *websocket.Conn, db *pgxpool.Pool, hub *gamemanager.Hub) {
	var currentUsername string
	for {
		var msg Message
		// Read JSON from the frontend
		err := conn.ReadJSON(&msg)
		if err != nil {
			break // Stop reading and fully disconnect the socket
		}

		fmt.Println("type: " + msg.Type + " code: " + msg.Code + " reason " + msg.Reason)

		if msg.Type == "authenticate" {
			currentUsername = msg.Token
            fmt.Println("WS authenticated for player: " + currentUsername)
			continue
		}

		if msg.Type == "create_room" {
			// Generate the code
			newCode := hub.CreateRoom() // TODO add room too db 
			
			fmt.Println("Code generated by the backend: " + newCode.ID) // I can't show this in the inspect element console, that needs to be implemented in the javascript

			// Store it in the DB 

			playerID := 42
			
			err = newCode.AddPlayer(playerID, "first", conn)
			if err != nil {
				fmt.Printf("Debug: fail join denied")
			}

			fmt.Printf("Debug: success join player")

			newCode.MessageChan <- gamemanager.Notification{
				PlayerID: playerID,
				Data: map[string]interface{}{
					"type": "room_created",
					"code": newCode.ID,
				},
			}

			newCode.MessageChan <- gamemanager.Notification{
				PlayerID: playerID,
				Data: map[string]interface{}{
					"type": "player_joined",
					"player": map[string]interface{}{
						"id":   playerID,
						"name": "first",
						"host": true,
					},
				},
			}

			newCode.BroadcastLobbyState()

			go func(roomID string) {
				query := "INSERT INTO rooms (room_code, created_at) VALUES ($1, NOW())"
				db.Exec(context.Background(), query, roomID)
				}(newCode.ID)
		}

		if (msg.Type == "join_room") {
			fmt.Printf("Room code for join_room is %d\n", msg.Code)
			room, err := hub.GetRoom(msg.Code)
			if (err != nil) {
				response := (Message{
					Type: "join_denied",
					Reason: "Invalid Room",
				})
				conn.WriteJSON(response)
				continue;
			}
			playerID := rand.IntN(1000) // NOTE tmp ID
			err = room.AddPlayer(playerID, "Second", conn); // TODO set correct ID
			
			if (err != nil) {
				response := (Message{
					Type: "join_denied",
					Reason: err.Error(),
				})
				conn.WriteJSON(response)
				continue;
			}

			room.MessageChan <- gamemanager.Notification{
				PlayerID: playerID,
				Data: Message{
					Type: "join_success",
					Code: room.ID,
				},
			}
		}
	}
}

/*
	Gorilla structure needed to switch the http protocol to a websocket
	has a read / write buffer, not unlike ft_irc, it can store incomplete in and out data
	checkOrigin makes sure that the origin of the request is on our own domain, currently it
		accepts all origins, but as websockets are vulnerable to cross site websocket hijacking,
			we need to set it to something more secure later.
*/

var upgrader = websocket.Upgrader{
	ReadBufferSize:  1024,
	WriteBufferSize: 1024,
	// Allow all origins for development; be stricter in production!
	CheckOrigin: func(r *http.Request) bool {
		return true
	},
}

/*
	upgrades the http protocol to a websocket, when its "done" it closes the connection
	"done" is defined by either the webpage being closed, or the websocket handler returned
*/

func handleWebsocket(c *gin.Context, db *pgxpool.Pool, hub *gamemanager.Hub) {
	// we upgrade the 
	conn, err := upgrader.Upgrade(c.Writer, c.Request, nil )
	if err != nil {
		return 
	}
	defer conn.Close()
	socketLogic(conn, db, hub)
}
